package com.github.adminfaces.starter.tables;

/**
 *
 * @author ritasantiago
 */
public class Movement {
    int id;
    String local, hour;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getLocal() {
        return local;
    }

    public void setLocal(String local) {
        this.local = local;
    }

    public String getHour() {
        return hour;
    }

    public void setHour(String hour) {
        this.hour = hour;
    }
    
}
